// --- CORE CONTROLLER CLASS ---
class AppI18nEngine {
  private static instance: AppI18nEngine;

  private constructor() { }

  public static getInstance(): AppI18nEngine {
    if (!AppI18nEngine.instance) {
      AppI18nEngine.instance = new AppI18nEngine();
    }
    return AppI18nEngine.instance;
  }

  // Priority: FORCE CHINESE
  public async detectLanguage(): Promise<LanguageCode> {
    return 'zh';
  }

  private mockApiFetch(): Promise<LanguageCode | null> {
    return new Promise(resolve => {
      setTimeout(() => resolve(null), 100); // Simulate network delay, return null to fall through
    });
  }
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const I18nProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<LanguageCode>('zh');
  const [isLoaded, setIsLoaded] = useState(false);

  useEffect(() => {
    // FORCE CHINESE INITIALIZATION
    setLanguageState('zh');
    setIsLoaded(true);
  }, []);

  const setLanguage = (lang: LanguageCode) => {
    // FORCE CHINESE
    setLanguageState('zh');
    localStorage.setItem('user_lang', 'zh');
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    // FORCE CHINESE DICTIONARY
    const dict = resources['zh'];
    let text = dict[key] || key;

    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(new RegExp(`{${k}}`, 'g'), String(v));
      });
    }
    return text;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, isLoaded }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
};
