class I18nManager {
  private currentLang: LangCode = 'zh_CN';

  setLanguage(lang: LangCode) {
    this.currentLang = lang;
  }

  getCurrentLanguage() {
    return this.currentLang;
  }

  t(key: string): string {
    // FORCE CHINESE: Always use zh_CN dictionary regardless of currentLang
    const dict = DICTIONARIES['zh_CN'];
    if (!dict || !dict[key]) {
        // Fallback to English dictionary (which we should also probably ensure is Chinese, but for now zh_CN is the source of truth)
        // If key is missing in zh_CN, we try to return the key itself or a placeholder
        return key;
    }
    return dict[key];
  }
}

